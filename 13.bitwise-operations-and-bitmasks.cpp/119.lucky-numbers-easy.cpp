bool isSuperLucky(int n) {
  int fours{};
  int sevens{};
  while (n) {
    int temp{n % 10};
    n /= 10;
    if (temp == 4) {
      ++fours;
    } else if (temp == 7) {
      ++sevens;
    } else {
      return false;
    }
  }

  return fours == sevens;
}

void genAll() {
  int count{};
  for (int i{1}; i < 1000000000; ++i) {
    if (isSuperLucky(i)) {
      std::cout << i << ", ";
      ++count;
    }
  }
  std::cout << count << '\n';
}
